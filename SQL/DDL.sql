CREATE TABLE members(
	username VARCHAR(50) PRIMARY KEY,
	passwrd VARCHAR(50)
	first_name VARCHAR(50)
	last_name VARCHAR(50)
	age INT
	gender VARCHAR(6)
	country VARCHAR(20)
)


CREATE TABLE trainers(
